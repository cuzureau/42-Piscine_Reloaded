int		ft_sqrt(int nb)
{
	int		i;
	int		odd;

	i = 0;
	odd = 1;
	if (nb < 1 || nb > 2147395600)
		return (0);
	while (nb != 0)
	{
		if (nb < 0)
			return (0);
		nb = nb - odd;
		odd = odd + 2;
		i++;
	}
	return (i);
}
